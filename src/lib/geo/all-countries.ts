// Search countries by name, ISO codes
export function searchCountries(query: string): Country[] {
  const q = query.toLowerCase().trim();
  if (!q) return ALL_COUNTRIES;
  
  return ALL_COUNTRIES.filter(c => 
    c.name.toLowerCase().includes(q) ||
    c.iso2.toLowerCase() === q ||
    c.iso3.toLowerCase() === q ||
    c.region?.toLowerCase().includes(q) ||
    c.subregion?.toLowerCase().includes(q)
  );
}

// Get country by any identifier
export function getCountry(identifier: string): Country | undefined {
  const id = identifier.toUpperCase().trim();
  return ALL_COUNTRIES.find(c => 
    c.iso2 === id || 
    c.iso3 === id || 
    c.name.toUpperCase() === id
  );
}

// Get coordinates for a country
export function getCountryCoordinates(iso2: string): { lat: number; lng: number } | undefined {
  return COUNTRY_COORDINATES[iso2.toUpperCase()];
}
